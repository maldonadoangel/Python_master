# importamos el modulo de matematica
import math

print(round(1.3))
print(round(1.5))

# Valor absoluto
print(abs(-55))


# buscar el numero más cercano
print(math.ceil(1.1))
print(math.floor(1.9999))
print(math.isnan(23))

# Elevar un número a la potencia
print(math.pow(10, 2))
# sacar la raiz de un número
print(math.sqrt(9))
