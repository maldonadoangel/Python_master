animal = "Chanchito Feliz"
"""Metodo para trasladar un metodo mayuscula"""
print(animal.upper())

"""Metodo para trasladar en minuscula"""
print(animal.lower())

"""Metodo para capitalizar"""
print(animal.capitalize())
"""Metodo de title"""
print(animal.title())

"""Metodo de buscar una cadena caracteres"""
print(animal.find("ch"))

"""Metodo para reemplzar"""

print(animal.replace("ch", "j"))
