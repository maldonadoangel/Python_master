# Transformar una lista a una tupla
punto = tuple([1, 2])
print(punto)
# para las tuplas no podemos eliminar datos

# para poder modificar una tupla la unica manera es cambiando una tupla por una lista
lista_numeros = list(numeros)
print(lista_numeros)
